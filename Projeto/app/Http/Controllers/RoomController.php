    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\Room  $room
     * @return \Illuminate\Http\Response
     */
    public function destroy(Room $room)
    {
        try {
            $room->delete();
            return redirect('room')->with('success', 'Sala excluída com sucesso!');
        } catch (Throwable $e) {
            report($e);
    
            return redirect('room')->with('danger', 'Sala  não excluída com sucesso!');
        }
    }
}
